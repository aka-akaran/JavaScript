

// push, pop are fast as compared to unshift, shift ;
// can push multiple items at same time



let name = ["akash", "pras", "rash"] ;
console.log(name) ;


// push --> insert at end
name.push(+"as", "k")
console.log(name) ;

// pop -->removes form back
let popped = name.pop() ;
console.log(popped) ;
console.log(name) ;

// unshift - insert at starting
name.unshift("guru") ;
console.log(name) ;

// shift removes from front ;
let shifted = name.shift() ;
console.log(shifted) ;
console.log(name) ;



